    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:

        boolean res = false;
        
        int cant = Integer.parseInt(txt_cantidadBebidas.getText());
        
        if(txt_codigoBebida.getText().equals("")){
        
            MostraError me = new MostraError();
            me.setVisible(true);

        }else{
        
        
            if(cbo_tamanoBebida.getSelectedItem().toString().charAt(0) == 'N'){
                res = ControlIngresoBebida.ingresarBebida(cbo_nombreBebida.getSelectedItem().toString(), 
                                                  cbo_tamanoBebida.getSelectedItem().toString().charAt(0), 
                                                  txt_codigoBebida.getText(), 2000, cant );
                int total = cant * 2000;
            
                txt_totalpagar.setText("$"+total+"");
            
            }else{
                res = ControlIngresoBebida.ingresarBebida(cbo_nombreBebida.getSelectedItem().toString(), 
                                                  cbo_tamanoBebida.getSelectedItem().toString().charAt(0), 
                                                  txt_codigoBebida.getText(), 4000, cant );

            int total = cant * 4000;
            
            txt_totalpagar.setText("$"+total+"");
            
            }
        }
        System.out.println("Resultado: "+res);
        
        
        
    }//GEN-LAST:event_btn_guardarActionPerformed

